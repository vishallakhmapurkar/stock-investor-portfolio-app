package com.impetus.stockapp.config;

/**
 * The Class AppConfig.
 * @author vishal.lakhmapurkar
 * @version $Revision: 1.0 $
 */
public class AppConfig {

    /** The web service path. */
    private String webServicePath;

    /**
     * Gets the web service path.
     * 
    
     * @return the web service path */
    public String getWebServicePath() {
	return webServicePath;
    }

    /**
     * Sets the web service path.
     * 
     * @param webServicePath
     *            the new web service path
     */
    public void setWebServicePath(final String webServicePath) {
	this.webServicePath = webServicePath;
    }

}
